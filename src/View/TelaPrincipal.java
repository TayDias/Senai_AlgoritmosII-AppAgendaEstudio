    private void btnPagoParcialmenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagoParcialmenteActionPerformed
        Pagamento p = new Pagamento();
        int linha = tblHorariosFinanceiro.getSelectedRow();
        int id = Integer.parseInt(tblHorariosFinanceiro.getValueAt(linha, 0).toString());

        PagamentoDAO dao = new PagamentoDAO();
        p = dao.getPagamentoById(id);

        double valor = p.getValor();
        double v2 = Double.parseDouble(txtPagoParcialmente.getText());

        try {
            double d = p.PagarParcialmente(valor, v2);
            p.setDescricao(p.getDescricao());       
            p.setValor(d);
            p.setIdCliente(p.getIdCliente());
            p.setCustoPorHora(p.getCustoPorHora());
            
            dao.update(p);
            atualizaPagamentos();

        } catch (ValorSuperiorException ex) {
            JOptionPane.showMessageDialog(this, "Valor Superior ao Custo Total!");
        }
      
        atualizaPagamentos();
        lblMensagemF.setText("Pago Parcialmente.");
    }//GEN-LAST:event_btnPagoParcialmenteActionPerformed

