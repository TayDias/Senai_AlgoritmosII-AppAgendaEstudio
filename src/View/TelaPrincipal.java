    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        //Agenda
        Agenda a = new Agenda() {};
        int i = Integer.parseInt((String) cbSala.getSelectedItem());

        a.setSala(i);
        AgendaDAO daoA = new AgendaDAO();
        daoA.insert(a);

        //Sala
        Cliente c = (Cliente) lstClientes.getSelectedValue();

        if(alterarA == false){

            Sala objSala = new Sala();

            objSala.setIdCliente(c.getId());
            objSala.setNumero(cbSala.getSelectedIndex()+1);

            //data
            try {
                objSala.setData(sdf.parse(txtData.getText()));
            } catch(ParseException e ) {
                e.printStackTrace();
            }
            //horario
            try {
                objSala.setHorarioInicio(sdf2.parse(txtHorarioInicial.getText()));
                objSala.setHorarioTermino(sdf2.parse(txtHorarioTermino.getText()));
            } catch(ParseException e ) {
                e.printStackTrace();
            }

            a.adicionarSala(objSala);

            //Cliente c = (Cliente) cbClientes.getSelectedItem();
            c = (Cliente) cSel;

            try {
                adicionarHorario(objSala);

            } catch (HorarioOcupadoException ex) {
                JOptionPane.showMessageDialog(this, "Horario já Ocupado!");
            }

            //banco de dados Sala
            SalaDAO dao = new SalaDAO();
            dao.insert(objSala);

            lblAgendado.setText("Horario Agendado");

            //Calcular horas/ Pagamento
            PagamentoDAO daoP = new PagamentoDAO();
            Pagamento p = new Pagamento();

            int d = diffHoras(objSala.getHorarioInicio(), objSala.getHorarioTermino());
            double valor = p.CalcularCustoAgendamento(d);

            p.setValor(valor);
            p.setDescricao("Agendamento de Sala");
            p.setIdCliente(c.getId());
            p.setCustoPorHora(p.getCustoPorHora());

            daoP.insert(p);

            atualizaPagamentos();
            //
        }
        else{

            hSel.setNumero(cbSala.getSelectedIndex()+1);
            //data
            try {
                hSel.setData(sdf.parse(txtData.getText()));
            } catch(ParseException e ) {
                e.printStackTrace();
            }
            //horario
            try {
                hSel.setHorarioInicio(sdf2.parse(txtHorarioInicial.getText()));
                hSel.setHorarioTermino(sdf2.parse(txtHorarioTermino.getText()));
            } catch(ParseException e ) {
                e.printStackTrace();
            }
            //hSel.setHorarioInicio(null);

            SalaDAO dao = new SalaDAO();
            dao.update(hSel);

            lblAgendado.setText("Horário Alterado.");

            //Calcular horas/ Pagamento
            diffHoras(hSel.getHorarioInicio(), hSel.getHorarioTermino());

        }

        mostrarTelaHorariosSala();

        // calcularValorTotal(agenda);
        calcularValorTotal();

        limparCampos();
        atualizaHorariosMarcados();
    }//GEN-LAST:event_btnAgendarActionPerformed

