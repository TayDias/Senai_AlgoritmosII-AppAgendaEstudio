       public int diffHoras(java.util.Date dataInicio, java.util.Date dataFim) {
        GregorianCalendar calInicio = new GregorianCalendar();
        GregorianCalendar calFim = new GregorianCalendar();

        calInicio.setTime(dataInicio);
        calFim.setTime(dataFim);

        // Calcula a diferença entre hora fim e a hora inicio
        long diff = calFim.getTimeInMillis() - calInicio.getTimeInMillis();

        // Quantidade de milissegundos em uma hora
        long tempoHora = 1000 * 60 * 60;

        return (int) (diff / tempoHora);
    }
    
