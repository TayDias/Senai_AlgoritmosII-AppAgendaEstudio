    public void adicionarHorario(Sala objSala) throws HorarioOcupadoException{
         SalaDAO dao = new SalaDAO();
         List<Sala> listarSala = dao.listarSala();
        
        //pega o modelo da Tabela e coloca na variavel "model"
         DefaultTableModel model = 
                (DefaultTableModel) this.tblHorariosMain.getModel();
        //insere na tabela o número de linhas que a lista tem
        model.setRowCount(listarSala.size());
        
        //laço para inserir os dados dos objetos na Tabela
        for (int i = 0; i < listarSala.size(); i++) {
            
            model.setValueAt(listarSala.get(i).getClass().getName(), i, 0);
            model.setValueAt(listarSala.get(i).getNumero(), i, 1);
            model.setValueAt(listarSala.get(i).getData(), i, 2);
            model.setValueAt(listarSala.get(i).getHorarioInicio(), i, 3);
            model.setValueAt(listarSala.get(i).getHorarioTermino(), i, 4);     
           
            int n = listarSala.get(i).getNumero();
            Date d = (Date) listarSala.get(i).getData();
            Time t1 = (Time) listarSala.get(i).getHorarioInicio();
            Time t2 = (Time) listarSala.get(i).getHorarioTermino();
            
         if(n == objSala.getNumero() && d == objSala.getData() && 
                 (t1== objSala.getHorarioInicio() || t2== objSala.getHorarioTermino())){
               throw new HorarioOcupadoException();
        }
         else{
             e = false;
         }
        }
       
    }
    
